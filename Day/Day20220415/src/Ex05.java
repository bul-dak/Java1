
public class Ex05 {

	public static void main(String[] args) {
		 for(int i=0; i<5; i++) {  // 0 1 2 3 4
	         for(int j=0; j<5; j++) {   // 0 1 2 3 4
	            if( i+j >=4 )
	               System.out.print("*");
	            else
	               System.out.print(" ");
	         }
	            
	         System.out.println();
		 }
	}

}
