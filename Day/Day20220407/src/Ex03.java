
public class Ex03 {

	public static void main(String[] args) {
		byte b = 127; //바이트는 -127~128
		char c = ' '; //공백넣어줘야함
		char answer = 'n';  //한 문자만 들어가야함 
		float f = 3.14f;  // 3.14뒤에 f넣어야함
		double d = 1.4e3f;  //f빼줘야함 f와 double의 자릿수 범위가 다르기 때문
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(answer);
		System.out.println(f);
		System.out.println(d);
	}

}
